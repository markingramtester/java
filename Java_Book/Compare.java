class Compare
{
	public static void main( String[] args ) //Example code demonstrating comparing values
	{
		float num1 = 24.75f ;
		int num2 = 25 ;
		
		System.out.println("Most is "+ Math.max(num1, num2)) ; //Returns highest number from stated variables
		System.out.println("Least is "+ Math.min(num1, num2)) ; //Returns lowest number from stated variables
	}
}
